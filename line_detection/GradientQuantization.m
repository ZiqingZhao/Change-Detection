[h w] = size(Ix);
mag = zeros(h,w);
direct = zeros(h,w);

%Step 2: compute the magnitude and direction
% Avoid boundary effect
for i=4:h-3
    for j=4:w-3
        ix = abs(Ix(i,j));
        iy = abs(Iy(i,j));
        mag(i,j)=ix+iy; folding = 1;
        if (ix==0) & (iy==0)
            direct(i,j)=-1;
            suppression(i,j)=-1;
        else
            while (ix<1) & (iy<1)
                folding = folding / 10;
                ix=ix*10;    iy = iy *10;
            end
            while (ix>32) | (iy>32)
                folding = folding * 2;
                ix = ix /2; iy = iy /2;
            end
            ix = floor(Ix(i,j)/folding);
            iy = floor(Iy(i,j)/folding);
            direct(i,j)=table(ix+33,iy+33);            
            suppression(i,j)=cannytable(ix+33,iy+33);
        end
    end
end


    for i=3:h-2
        for j=3:w-2
            if mag(i,j)<highThreshold;
                mag(i,j)=0;    
            end
        end
    end
else
    %Step 3: Canny Nonmaximum Suppression
    for i=3:h-2
        for j=3:w-2
            if (mag(i,j)<lowThreshold)
                mag(i,j)=0;      
            elseif (suppression(i,j)==1) | (suppression(i,j)==5)
                if (mag(i,j)<mag(i+1,j+1)) | (mag(i,j)<mag(i-1,j-1))
                    mag(i,j)=0;
                end
            elseif (suppression(i,j)==0) | (suppression(i,j)==4)    
                if (mag(i,j)<mag(i+1,j)) | (mag(i,j)<mag(i-1,j))
                    mag(i,j)=0;
                end
            elseif (suppression(i,j)==3) | (suppression(i,j)==7)
                if (mag(i,j)<mag(i+1,j-1)) | (mag(i,j)<mag(i-1,j+1))
                    mag(i,j)=0;
                end
            elseif (suppression(i,j)==2) | (suppression(i,j)==6)
                if (mag(i,j)<mag(i,j-1)) | (mag(i,j)<mag(i,j+1))
                    mag(i,j)=0;
                end
            elseif (mag(i,j)<highThreshold) % the magnitude is between highThreshold and lowThreshold
                if (mag(i-1,j-1)<highThreshold) & (mag(i-1,j)<highThreshold) &(mag(i-1,j+1)<highThreshold) & (mag(i,j-1)<highThreshold) &(mag(i,j+1)<highThreshold) & (mag(i+1,j-1)<highThreshold) & (mag(i+1,j)<highThreshold) &(mag(i+1,j+1)<highThreshold)
                    mag(i,j)=0;    
                else
                    mag(i,j)=highThreshold;
                end
            end
        end
    end
